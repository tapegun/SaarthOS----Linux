#define BAR_PLANE_SIZE   18*320/4  // # of pixels each plane need for bar


#include "text.h"

/*
    generate_status_bar
      DESPRIPTION: takes in string as input and generate a bar buffer fitting the
                   lower 320*18 part of the screen in plane order 0, 1, 2, 3
      INPUTS: output: pointer to the output status bar buffer
              input: pointer to the input string
      RETURN VALUE: NONE
      SIDE EFFECTS: generate the status bar buffer
*/
void generate_status_bar(unsigned char * output, char * input)
{
  char buffer[BAR_PLANE_SIZE * 4]; // create a temperary string to move data around
  int len = strlen(input);
  // 320 used frequently, this is the x_dimention of screen
  int spaceColNum = (320 - len * FONT_WIDTH)/2; //the space I need in the front and back

  int i, j;
  //first generate a buffer in the order of display on screen
  for (i = 0; i < 320; i++) // fill the top line of the buffer with blank
  {
    buffer[i] = BAR_COLOR; // fill with palette color 0x00
  }
  for (i = 0; i < len; i++) // traverse through the string and generate the buffer
  {
    char a = input[i]; // get the character one by one
    int b = a; // get the ASCII value of that character
    for (j = 0; j < FONT_HEIGHT; j++) // traverse through each row in the font data and generate data for the character
    {
      int p;
      int s = font_data[b][j]; // get the one-bite data
      for (p = 0; p < FONT_WIDTH; p++) // traverse through each pixel of the line
      {
        int row = 1 + j;
        int col = spaceColNum + (FONT_WIDTH * i) + p; // get the row and column number of the pixel
        int spot = 320 * row + col; // get the place on the buffer I want write to
        int q = s >> (7 - p); // move the bit I want to the LSB
        if (q & 1) // if the bit is one
        {
          buffer[spot] = TEXT_COLOR; // fill the pixel with some random color, can be switched
        }
        else
        {
          buffer[spot] = BAR_COLOR; // fill the pixel same as blank color
        }
      }
      // finished filling in the current row data for characters, put space at both front and tail
      if (i == 0)
      {
        int odd1, odd2; // to index for for loop
        for (odd1 = 0; odd1 < 2; odd1++)
        {
          for (odd2 = 0; odd2 < spaceColNum; odd2++)
          {
            int row = 1 + j;
            int col = odd1 * (320 - spaceColNum) + odd2; // odd1 determines whether in the front or back
            int spot = 320 * row + col; // get the place on the buffer I want write to
            buffer[spot] = BAR_COLOR; // fill the pixel same as blank color
          }
        }
      }
    }
  }
  // finished filling in the characters, fill the bottom row with blank color
  for (i = 5440; i < 5760; i++) // index to the bottom row (starting from 4 * BAR_PLANE_SIZE - 320 = 5440)
  {
    buffer[i] = BAR_COLOR; // fill with palette color 0x00
  }

  //buffer finished building, move the data into output, in the order of
  //plane 0, 1, 2, 3
  for (i = 0; i < 4; i++)
  {
    for (j = 0; j < BAR_PLANE_SIZE; j++)
    {
      int output_i = i * BAR_PLANE_SIZE + j; // index to the spot to copy the data in output
      int buffer_i = i + j * 4; // index to the corresponding spot in buffer
      output[output_i] = buffer[buffer_i];
    }
  }
}


/*
    generate_caption
      DESPRIPTION: takes in string as input and generate a mask for the text
      INPUTS: output: pointer to the output mask (13*8*16 for mask size)
              input: pointer to the input string
      RETURN VALUE: NONE
      SIDE EFFECTS: generate the text mask
*/
void generate_caption(unsigned char * output, char * input)
{
  int len = strlen(input);
  int spaceColNum = (CAPTION_TEXT_NUM - len)*FONT_WIDTH/2; //the space I need in the front and back

  int i, j;
  // generate a buffer in the order of display on screen
  for (i = 0; i < len; i++) // traverse through the string and generate the buffer
  {
    char a = input[i]; // get the character one by one
    int b = a; // get the ASCII value of that character
    for (j = 0; j < FONT_HEIGHT; j++) // traverse through each row in the font data and generate data for the character
    {
      int p;
      int s = font_data[b][j]; // get the one-bite data
      for (p = 0; p < FONT_WIDTH; p++) // traverse through each pixel of the line
      {
        int row = j;
        int col = spaceColNum + (FONT_WIDTH * i) + p; // get the row and column number of the pixel
        int spot = CAPTION_X_DIM * row + col; // get the place on the buffer I want write to
        int q = s >> (7 - p); // move the bit I want to the LSB
        if (q & 1) // if the bit is one
        {
          output[spot] = 0x01; // fill the pixel with some random color, can be switched
        }
        else
        {
          output[spot] = 0x00; // fill the pixel same as blank color
        }
      }
      // finished filling in the current row data for characters, put space at both front and tail
      if (i == 0)
      {
        int odd1, odd2; // to index for for loop
        for (odd1 = 0; odd1 < 2; odd1++)
        {
          for (odd2 = 0; odd2 < spaceColNum; odd2++)
          {
            int row = j;
            int col = odd1 * (CAPTION_X_DIM - spaceColNum) + odd2; // odd1 determines whether in the front or back
            int spot = CAPTION_X_DIM * row + col; // get the place on the buffer I want write to
            output[spot] = 0x00; // fill the pixel same as blank color
          }
        }
      }
    }
  }
}

